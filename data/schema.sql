CREATE TABLE server_temp (
    id INTEGER PRIMARY KEY AUTOINCREMENT,
    name varchar(255) NOT NULL,
    time DATETIME NOT NULL,
    value REAL NOT NULL
);

CREATE TABLE room_temp (
    id INTEGER PRIMARY KEY AUTOINCREMENT,
    name VARCHAR(255) NOT NULL,
    time DATETIME NOT NULL,
    value REAL NOT NULL
);

CREATE TABLE room_humidity (
    id INTEGER PRIMARY KEY AUTOINCREMENT,
    name VARCHAR(255) NOT NULL,
    time DATETIME NOT NULL,
    value REAL NOT NULL
);
